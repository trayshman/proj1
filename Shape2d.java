/**
* Interface that defines a two-dimensional shape.
*
* @author Christine Reilly
*/
public interface Shape2d {

  /**
  * Calculates the perimiter of this shape.
  *
  * @return The perimiter of this shape.
  */
  public double perimeter();

  /**
  * Calculates the area of this shape.
  *
  * @return The area of this shape.
  */
  public double area();

}
